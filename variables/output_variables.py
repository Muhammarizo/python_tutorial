# pythonda chop etish uchun print() funksiyasidan foydalaniladi. O'zgaruvchilarni chop etish uchun, o'zgaruvchi nomini print() funksiyasining ichiga yozish kifoya qiladi.
x = "I am learning Python"
print(x)

# print() funksiyasi orqali bir nechta o'zgaruvchini bir vaqtda chop etish mumkin. Buning uchun, o'zgaruvchilarni vergul bilan ajratib yozish kifoya qiladi.
x = "Python"
y = "is"
z = "awesome"
print(x, y, z)

# print() funksiyasi orqali o'zgaruvchilarni birlashtirish mumkin. Buning uchun, o'zgaruvchilarni + operatori bilan birlashtirish kifoya qiladi.
x = "Python "
y = "is "
z = "awesome"
print(x + y + z)

# vergul va + operatori o'rtasidagi farq shundaki, vergul o'zgaruvchilarni bo'sh joy bilan ajratadi, + operatori esa o'zgaruvchilarni birlashtiradi. Agar siz + operatorini ishlatsangiz, o'zgaruvchilar orasida bo'sh joy qo'shishingiz kerak bo'ladi.

