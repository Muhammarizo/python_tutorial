
# Shunday qilib aytganda, Python-da siz o'zgaruvchiga ma'lum bir turni belgilashni xohlashingiz mumkin.
# Bu casting orqali amalga oshiriladi. Python ob'ektga yo'naltirilgan til bo'lib, u ma'lumot turlarini, 
# shu jumladan uning primitiv turlarini aniqlash uchun sinflardan foydalanadi.


# int() - butun sonni butun son literalidan, float literalidan (barcha onliklarni olib tashlash orqali) yoki string literalidan (agar string butun sonni ifodalayotgan bo'lsa) yaratadi.

# float() - butun son literalidan, float literalidan yoki string literalidan float son yaratadi (agar string float yoki butun sonni ifodalayotgan bo'lsa).

# str() - turli xil ma'lumot turlaridan, jumladan stringlar, butun son literalari va float literalari kabi string yaratadi.

# Integers
x = int(1)  # x butun son 1 ga teng
y = int(2.8)  # y butun son 2 ga teng
z = int("3")  # z butun son 3 ga teng

# Floats
a = float(1)  # a float son 1.0 ga teng
b = float(2.8)  # b float son 2.8 ga teng
c = float("3")  # c float son 3.0 ga teng
w = float("4.2")  # w float son 4.2 ga teng

# Strings
x = str("Hello World")  # s1 string "Hello World" ga teng
y = str(2)           # s2 string "2" ga teng
z = str(3.0)         # s3 string "3.0" ga teng