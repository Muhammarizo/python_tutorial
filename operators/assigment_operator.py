# Qisqartirilgan operatorlar

# x = 5 
# x += 4     x = x + 4
# x -= 4     x = x - 4
# x *= 4.    x = x * 4
# x /= 4.    x = x / 4
# x //= 4.   x = x // 4
# x %= 4     x = x % 4
# x **= 4.   x = x ** 4

x = 10

x += 5    # x = 15
x -= 3    # x = 12
x *= 2    # x = 24
x /= 4    # x = 6.0
x //= 2   # x = 3.0
x %= 2    # x = 1.0
x **= 3   # x = 1.0